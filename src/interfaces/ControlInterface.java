    public void updateButtonGroup(int activeIndex) {
        activeButtonGroup[activeIndex] = true;
        for (int i = 0; i < activeButtonGroup.length; i++) {
            activeButtonGroup[i] = false;
            if (i == activeIndex){
                activeButtonGroup[i] = true;
            }
        }
        for (int i = 0; i < activeButtonGroup.length; i++) {
            if (activeButtonGroup[i]){
                buttonGroup[i].setBackground(MAIN_COLOR);
                labelGroup[i].setForeground(Color.WHITE);
            }else{
                buttonGroup[i].setBackground(BACKGROUND_COLOR);
                labelGroup[i].setForeground(PRIMARY_TEXT);
            }
        }
    }
    
    private void centreWindows() {
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();

        int windownsGap = 30;
        int horizontalGap =  (int) ((dimension.getWidth() - 
            graphSketch.SKETCH_WIDTH - this.getWidth() - windownsGap) / 2);
        
        this.setLocation(horizontalGap, (int) 
            ((dimension.getHeight() - this.getHeight()) / 2));
        
        graphSketch.getSurface().setLocation(
            horizontalGap + this.getWidth() + windownsGap, 
            (int) ((dimension.getHeight() - graphSketch.SKETCH_HEIGHT) / 2));
    }

